    private void generarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarFacturaActionPerformed
        //variables para recuperar datos
        ResultSet rs; //iniciando nuestro contenedor
        Proveedor objEc=new Proveedor();
        BD_REPORTES objNc= new BD_REPORTES();
        objEc.setRazonsocial(txt_bus.getText());
        rs=objNc.mostrarprov(objEc); //rs contiene el resultado        
        try {
            while (rs.next()) //inicia desde el primer elemento hasta el ultimo
            {
                txt_razon.setText(rs.getString("razonsocial"));               
                txt_ruc.setText(rs.getString("ruc")); 
                txt_direc.setText(rs.getString("direccion")); 
                txt_telf.setText(rs.getString("telefono"));
            }
            
        } catch (SQLException ex) {
            System.out.println(ex);           
        } 
        
    }//GEN-LAST:event_generarFacturaActionPerformed

