    private void generarFactura1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarFactura1ActionPerformed
        // TODO add your handling code here:
        double totall=0,subtotall=0,igv=0;
        int validacion = 0;//variables
        String id = "", fecha = "", telefono = "", razonsocial = "", pass = "", direccion = "",nrocuenta="";
        int ruc=0;
        String fechames;
        DateFormat df1= new SimpleDateFormat("dd-MM-yyyy");
        fechames=df1.format(txt_fecha.getDate()); 
        //recuperar valores dentro de los texfile
        totall=Double.valueOf(txt_total.getText());
        igv=totall*0.18;
        subtotall=totall-igv;
        id = txt_bus.getText().trim();//metodo trim eliminamos los espacios que estan al inicio y al final de la cadena de texto
        
        //se cambian de color a rojo
        //validamos los campos antes de enviar los datos        
        if (validacion == 0) {
            //JOptionPane.showMessageDialog(rootPane, "Datos ingresados correctamente");

            txt_bus.setBackground(Color.white);            

            //Enviamos los datos obtenidos a las clases que hemos creado
            Facturacompras usuario = new Facturacompras();           
            bd_Conexion con = new bd_Conexion();
            
            usuario.setIdproveedor(Integer.valueOf(id));
            usuario.setTotal(totall);
            usuario.setSubtotal(subtotall);
            usuario.setFecha(fechames);            

            if (con.insertarfaccompr(usuario)) {
                JOptionPane.showMessageDialog(null, "Factura Registrada corecctamente");
                Limpiar();

            } else {
                JOptionPane.showMessageDialog(null, "Error Contacte con el Administrador");
            }
            

        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe de completar las casillas de rojo");
        }        
        
    }//GEN-LAST:event_generarFactura1ActionPerformed

