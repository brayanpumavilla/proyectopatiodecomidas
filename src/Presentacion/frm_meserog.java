    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed
        // TODO add your handling code here:
        int numeroDeMesero;
        String nombre, telefono, mail, username, contra;
        //Datos Obtenidos de las casillas del dise単o de los txt
        // montoTotal =Double.parseDouble(txtPrecioTotal.getText());

        nombre = txt_nombre.getText();
        mail = txt_mail.getText();
        username = txt_username.getText();
        int validacion = 0;
        contra = txt_contrasena.getText();
        numeroDeMesero = cmb_numerodeMesero.getSelectedIndex();
        telefono = txt_telefono.getText();
        if (nombre.equals("")) {
            txt_nombre.setBackground(Color.red);
            validacion++;
        } else {
            txt_nombre.setBackground(Color.white);
        }
        if (mail.equals("")) {
            txt_mail.setBackground(Color.red);
            validacion++;
        } else {
            txt_mail.setBackground(Color.white);
        }
        if (username.equals("")) {
            txt_username.setBackground(Color.red);
            validacion++;
        } else {
            txt_username.setBackground(Color.white);
        }
        if (telefono.equals("")) {
            txt_telefono.setBackground(Color.red);
            validacion++;
        } else {
            txt_telefono.setBackground(Color.white);
        }
        if (contra.equals("")) {
            txt_contrasena.setBackground(Color.red);
            validacion++;
        } else {
            txt_contrasena.setBackground(Color.white);
        }

        if (validacion == 0) {

            try {
                Connection cn = conexion.Conectar();
                PreparedStatement pst = cn.prepareStatement(
                    "update mesero set nombre=?,telefono=?,mail=?,contrase単a=?,userName=?,numeroMesero=?"
                    + "where idmesero = '"+ID+"'");
                pst.setString(1, nombre);
                pst.setString(2, telefono);
                pst.setString(3, mail);
                pst.setString(4, contra);
                pst.setString(5, username);
                pst.setInt(6, numeroDeMesero);
                pst.executeUpdate();
                cn.close();
                JOptionPane.showMessageDialog(null, "Modificacion correcta");
            } catch (SQLException e) {
                System.err.println(e);
            }

            Limpiar();
        }

    }//GEN-LAST:event_btn_ActualizarActionPerformed

